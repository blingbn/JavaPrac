package prac;

public class Person {
	
	String name;
	int age;
	
	public Person(String n, int a) {
		name = n;
		age = a;
	}
	public String getName() {
		return name;
	}
	public void setName(String n) {
		this.name = n;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int a) {
		this.age = a;
	}
	
	
}
