
public class Ex04_6 {

	public static void main(String[] args) {
		// 문자열을 이용한 witch문
		
		String month = "10월";		
		
		switch(month) {
		case "1월" : System.out.println("겨울"); break;
		case "3월" : System.out.println("봄"); break;
		case "7월" : System.out.println("여름"); break;
		case "10월" : System.out.println("가을"); break;
		}//end switch
		

	}

}
